print('***CICLO FOR***')
cadena= "Benvindos a Brasil"
#iteramos (o recorremos) cada caracter de la cadena
for letra in cadena:
    print(letra, end=" ")

print('\n')

#iteramos (o recorremos) cada elemento de la lista
frutas='banana','naranja','uva','pera'
for frutos in frutas:
    print(frutos,end=' ')

